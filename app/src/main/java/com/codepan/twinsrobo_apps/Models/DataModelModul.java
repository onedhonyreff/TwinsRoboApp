package com.codepan.twinsrobo_apps.Models;

public class DataModelModul {
    private Integer modul_image;
    private String modul_title;
    private String modul_price;
    private String modul_status;

    public Integer getModul_image() {
        return modul_image;
    }

    public void setModul_image(Integer modul_image) {
        this.modul_image = modul_image;
    }

    public String getModul_title() {
        return modul_title;
    }

    public void setModul_title(String modul_title) {
        this.modul_title = modul_title;
    }

    public String getModul_price() {
        return modul_price;
    }

    public void setModul_price(String modul_price) {
        this.modul_price = modul_price;
    }

    public String getModul_status() {
        return modul_status;
    }

    public void setModul_status(String modul_status) {
        this.modul_status = modul_status;
    }
}
